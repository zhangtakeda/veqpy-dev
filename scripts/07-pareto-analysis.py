def build_surface_comparison_data(
    all_samples: dict[str, list[Sample]],
    *,
    backend: str,
    test_nr: int,
    test_nt: int,
) -> dict[str, dict[str, object]]:
    benchmark = load_benchmark(backend)
    data: dict[str, dict[str, object]] = {}
    for case_key in CASE_KEYS:
        samples = finite_non_reference_samples(all_samples[case_key])
        representative = fastest_case_below_error(samples, threshold=REPRESENTATIVE_ERROR_THRESHOLD)
        if representative is None:
            continue
        reference = build_reference_case(benchmark, case_key)
        grid = benchmark.Grid(
            Nr=int(test_nr),
            Nt=int(test_nt),
            quadrature_scheme="legendre",
            L_max=int(benchmark.REFERENCE_GRID.L_max),
            M_max=int(benchmark.REFERENCE_GRID.M_max),
        )
        representative_case = build_pf_case(benchmark, reference, grid, representative.signature)
        _, representative_equilibrium, _ = solve_with_timing(
            benchmark,
            representative_case,
            grid,
            repeat_count=1,
            method=CASE_SOLVER_METHODS[case_key],
            solver_config=get_case_solver_config(benchmark, case_key),
        )
        representative_surface_equilibrium = resample_surface_equilibrium(
            representative_equilibrium
        )
        data[case_key] = {
            "high_equilibrium": reference.exact_equilibrium,
            "representative_equilibrium": representative_surface_equilibrium,
            "high_parameter_count": exact_reference_parameter_count(
                all_samples[case_key], case_key
            ),
            "representative_parameter_count": int(representative.parameter_count),
        }
    return data


def render_surface_comparison_row(
    axes: list[plt.Axes],
    surface_data: dict[str, dict[str, object]],
) -> None:
    for col, case_key in enumerate(CASE_KEYS):
        ax = axes[col]
        case_data = surface_data.get(case_key)
        if case_data is None:
            ax.set_axis_off()
            continue
        high_surfaces = build_surfaces_from_equilibrium(
            case_data["high_equilibrium"], SURFACE_LEVELS
        )
        representative_surfaces = build_surfaces_from_equilibrium(
            case_data["representative_equilibrium"], SURFACE_LEVELS
        )
        high_label = f"{int(case_data['high_parameter_count'])} params"
        representative_label = f"{int(case_data['representative_parameter_count'])} params"
        _plot_surface_pair(
            ax,
            case_key=case_key,
            high_surfaces=high_surfaces,
            representative_surfaces=representative_surfaces,
            high_label=high_label,
            representative_label=representative_label,
        )
        _style_axis(
            ax,
            title=SURFACE_COMPARE_TITLES[case_key],
            xlabel="R [m]",
            ylabel="Z [m]",
            yscale="linear",
        )
        curves = list(high_surfaces.values()) + list(representative_surfaces.values())
        limits = compute_rz_limits(curves)
        x0, x1 = limits[0]
        xpad_extra_fraction = (
            SURFACE_COMPARE_FIRST_XPAD_EXTRA_FRACTION
            if col == 0
            else SURFACE_COMPARE_XPAD_EXTRA_FRACTION
        )
        ax.set_xlim(x0, x1 + (x1 - x0) * xpad_extra_fraction)
        ax.set_ylim(*limits[1])
        ax.set_aspect("equal")
        ax.legend(
            loc=SURFACE_COMPARE_LEGEND_LOC,
            fontsize=scaled_font_size(LEGEND_FONT_SIZE),
            frameon=LEGEND_FRAME_ON,
            columnspacing=LEGEND_COLUMN_SPACING,
            labelspacing=LEGEND_LABEL_SPACING,
        )


def _plot_surface_pair(
    ax: plt.Axes,
    *,
    case_key: str,
    high_surfaces: dict[float, np.ndarray],
    representative_surfaces: dict[float, np.ndarray],
    high_label: str,
    representative_label: str,
) -> None:
    for idx, level in enumerate(sorted(high_surfaces)):
        linewidth = (
            SURFACE_COMPARE_LINE_WIDTH
            if level < 1.0 - 1.0e-12
            else SURFACE_COMPARE_BOUNDARY_LINE_WIDTH
        )
        high_curve = close_curve(high_surfaces[level])
        rep_curve = close_curve(representative_surfaces[level])
        ax.plot(
            rep_curve[:, 0],
            rep_curve[:, 1],
            color=CASE_COLORS[case_key],
            linestyle=SURFACE_COMPARE_REPRESENTATIVE_STYLE,
            linewidth=linewidth,
            label=(representative_label if idx == 0 else None),
            zorder=2,
        )
        ax.plot(
            high_curve[:, 0],
            high_curve[:, 1],
            color=SURFACE_COMPARE_REFERENCE_COLOR,
            linestyle=SURFACE_COMPARE_REFERENCE_STYLE,
            linewidth=linewidth * SURFACE_COMPARE_REFERENCE_SCALE,
            label=(high_label if idx == 0 else None),
            zorder=0,
        )


def render(
    all_samples: dict[str, list[Sample]],
    frontiers: dict[str, list[Sample]],
    png_path: str,
    pdf_path: str,
    test_nr: int,
    test_nt: int,
    backend: str,
) -> None:
    png_dir = os.path.dirname(png_path)
    pdf_dir = os.path.dirname(pdf_path)
    if png_dir:
        os.makedirs(png_dir, exist_ok=True)
    if pdf_dir:
        os.makedirs(pdf_dir, exist_ok=True)
    apply_plot_style()
    surface_data = build_surface_comparison_data(
        all_samples,
        backend=backend,
        test_nr=test_nr,
        test_nt=test_nt,
    )
    fig = plt.figure(
        figsize=FIGURE_SIZE,
        constrained_layout=FIGURE_CONSTRAINED_LAYOUT,
    )
    grid_spec = fig.add_gridspec(
        FIGURE_NROWS,
        FIGURE_NCOLS,
        height_ratios=(0.8, 1.2),
        wspace=FIGURE_WSPACE,
        hspace=FIGURE_HSPACE,
    )
    ax_time = fig.add_subplot(grid_spec[0, 0:3])
    ax_param = fig.add_subplot(grid_spec[0, 3:6], sharey=ax_time)
    surface_axes = [
        fig.add_subplot(grid_spec[1, 0:2]),
        fig.add_subplot(grid_spec[1, 2:4]),
        fig.add_subplot(grid_spec[1, 4:6]),
    ]
    fig.set_constrained_layout_pads(wspace=FIGURE_WSPACE, hspace=FIGURE_HSPACE)
    max_parameter_count = 1
    time_legend_line_handles: dict[str, object] = {}
    time_legend_marker_handles: dict[str, object] = {}
    for case_key in CASE_KEYS:
        samples = [
            sample
            for sample in all_samples[case_key]
            if np.isfinite(sample.aggregate_rel_error)
            and sample.aggregate_rel_error > 0.0
            and not sample.is_exact_reference
        ]
        time_frontier = frontiers[case_key]
        parameter_frontier = parameter_error_frontier(all_samples[case_key])
        if not samples:
            continue
        samples_for_parameter_axis = [
            sample for sample in samples if int(sample.parameter_count) <= PARAMETER_COUNT_PLOT_MAX
        ]
        if samples_for_parameter_axis:
            max_parameter_count = max(
                max_parameter_count,
                max(int(sample.parameter_count) for sample in samples_for_parameter_axis),
            )

        representative = fastest_case_below_error(samples, threshold=REPRESENTATIVE_ERROR_THRESHOLD)
        _print_case_frontier_summary(
            case_key,
            time_frontier=time_frontier,
            parameter_frontier=parameter_frontier,
            representative=representative,
        )
        line_handle, marker_handle = _plot_time_case(
            ax_time,
            case_key=case_key,
            samples=samples,
            time_frontier=time_frontier,
            representative=representative,
        )
        if line_handle is not None:
            time_legend_line_handles[case_key] = line_handle
        if marker_handle is not None:
            time_legend_marker_handles[case_key] = marker_handle
        _plot_parameter_case(
            ax_param,
            case_key=case_key,
            samples=samples,
            parameter_frontier=parameter_frontier,
            representative=representative,
        )

    ax_time.set_xscale("log")
    ax_time.xaxis.set_major_locator(FixedLocator([1.0, 10.0, 100.0, 1000.0]))
    ax_time.xaxis.set_major_formatter(LogFormatterMathtext())
    ax_time.set_xticks(
        [1.0, 10.0, 100.0, 1000.0],
        labels=[r"$10^0$", r"$10^1$", r"$10^2$", r"$10^3$"],
    )
    ax_time.set_xlim(TIME_XMIN, TIME_XMAX)
    ax_time.set_ylim(bottom=ERROR_AXIS_YMIN, top=ERROR_AXIS_YMAX)
    _style_axis(
        ax_time,
        title=r"$\bf{(a)}$ Time vs Error",
        xlabel="average solve time [ms]",
        ylabel="shape error [m]",
    )
    remove_minor_ticks(ax_time)
    _style_case_marker_legend(ax_time, time_legend_line_handles, time_legend_marker_handles)

    ax_param.set_ylim(bottom=ERROR_AXIS_YMIN, top=ERROR_AXIS_YMAX)
    right_limit = min(
        PARAMETER_COUNT_PLOT_MAX,
        max_parameter_count + max(2, int(round(max_parameter_count * 0.04))),
    )
    ax_param.set_xlim(0.0, float(right_limit))
    ax_param.xaxis.set_major_locator(MaxNLocator(nbins=8, integer=True))
    _style_axis(
        ax_param,
        title=r"$\bf{(b)}$ Parameter Count vs Error",
        xlabel="parameter count",
    )
    remove_minor_ticks(ax_param)
    render_surface_comparison_row(surface_axes, surface_data)

    fig.savefig(png_path, dpi=SAVE_DPI, transparent=SAVE_TRANSPARENT)
    fig.savefig(pdf_path, dpi=SAVE_DPI, transparent=SAVE_TRANSPARENT)
    plt.close(fig)


def write_json(
    all_samples: dict[str, list[Sample]],
    frontiers: dict[str, list[Sample]],
    json_stem: str,
    test_nr: int,
    test_nt: int,
    backend: str,
    sweep_mode: str,
) -> None:
    def build_payload(
        samples_by_case: dict[str, list[Sample]],
        frontiers_by_case: dict[str, list[Sample]],
    ) -> dict[str, object]:
        return {
            "sweep": {
                "route": "PF",
                "coordinate": "psin",
                "input_kind": "uniform",
                "constraint": "Ip",
                "aggregate_metric": AGGREGATE_METRIC_ID,
                "error_unit": ERROR_UNIT,
                "test_nr": int(test_nr),
                "test_nt": int(test_nt),
                "backend": backend,
                "sweep_mode": str(sweep_mode),
                "signature_set": SIGNATURE_SET_IDS[str(sweep_mode)],
                "progression_rule": (
                    "Table 5 selected configurations only: three reduced-order rows per case"
                    if str(sweep_mode) == "partial"
                    else (
                        "scheme-A exhaustive sweep with a single shared core level, monotone contiguous "
                        "Fourier shells, and compact family-wise selected neighborhoods"
                    )
                ),
                "strategies": (
                    ["table05_selected"]
                    if str(sweep_mode) == "partial"
                    else ["scheme_a_full", "scheme_a_full_selected"]
                ),
                "length_bounds": {
                    case_key: {
                        "label": CASE_LABELS[case_key],
                        "min": dict(get_case_length_bounds(case_key)[0]),
                        "max": dict(get_case_length_bounds(case_key)[1]),
                    }
                    for case_key in samples_by_case
                },
            },
            "samples": {
                case_key: [asdict(sample) for sample in samples]
                for case_key, samples in samples_by_case.items()
            },
            "frontier": {
                case_key: [asdict(sample) for sample in frontiers_by_case.get(case_key, [])]
                for case_key in samples_by_case
            },
        }

    for case_key in all_samples:
        case_json_path = json_output_path(json_stem, case_key, sweep_mode)
        case_json_dir = os.path.dirname(case_json_path)
        if case_json_dir:
            os.makedirs(case_json_dir, exist_ok=True)
        case_payload = build_payload(
            {case_key: list(all_samples.get(case_key, []))},
            {case_key: list(frontiers.get(case_key, []))},
        )
        with open(case_json_path, "w", encoding="utf-8") as f:
            json.dump(case_payload, f, indent=2)


def load_cached_samples_from_path(path: str) -> dict[str, dict[str, Sample]]:
    if not os.path.exists(path):
        return {}
    try:
        with open(path, "r", encoding="utf-8") as f:
            payload = json.load(f)
    except (OSError, json.JSONDecodeError):
        return {}
    sweep = payload.get("sweep")
    if not isinstance(sweep, dict):
        return {}
    if (
        sweep.get("aggregate_metric") != AGGREGATE_METRIC_ID
        or sweep.get("error_unit") != ERROR_UNIT
    ):
        return {}
    samples_block = payload.get("samples")
    if not isinstance(samples_block, dict):
        return {}
    cache: dict[str, dict[str, Sample]] = {}
    for case_key, entries in samples_block.items():
        if not isinstance(case_key, str) or not isinstance(entries, list):
            continue
        case_cache = cache.setdefault(case_key, {})
        for entry in entries:
            if not isinstance(entry, dict):
                continue
            try:
                sample = Sample(**entry)
            except TypeError:
                continue
            if sample.is_exact_reference:
                continue
            case_cache[sample_signature_key(case_key, sample.signature)] = sample
        if not case_cache:
            cache.pop(case_key, None)
    return cache


def load_cached_samples(
    json_stem: str,
    sweep_mode: str,
) -> dict[str, dict[str, Sample]]:
    cache: dict[str, dict[str, Sample]] = {}
    candidate_paths = [json_output_path(json_stem, case_key, sweep_mode) for case_key in CASE_KEYS]
    for path in candidate_paths:
        partial_cache = load_cached_samples_from_path(path)
        for case_key, samples in partial_cache.items():
            cache.setdefault(case_key, {}).update(samples)
    return cache


def _load_sample_list(entries: object) -> list[Sample] | None:
    if not isinstance(entries, list):
        return None
    samples: list[Sample] = []
    for entry in entries:
        if not isinstance(entry, dict):
            return None
        try:
            samples.append(Sample(**entry))
        except TypeError:
            return None
    return samples


def load_plot_data_bundle(
    json_stem: str,
    sweep_mode: str,
) -> tuple[dict[str, list[Sample]] | None, dict[str, list[Sample]] | None]:
    all_samples: dict[str, list[Sample]] = {}
    all_frontiers: dict[str, list[Sample]] = {}
    for case_key in CASE_KEYS:
        path = json_output_path(json_stem, case_key, sweep_mode)
        if not os.path.exists(path):
            return None, None
        try:
            with open(path, "r", encoding="utf-8") as f:
                payload = json.load(f)
        except (OSError, json.JSONDecodeError):
            return None, None
        if not isinstance(payload, dict):
            return None, None
        sweep = payload.get("sweep")
        if not isinstance(sweep, dict):
            return None, None
        if (
            sweep.get("aggregate_metric") != AGGREGATE_METRIC_ID
            or sweep.get("error_unit") != ERROR_UNIT
        ):
            return None, None
        if sweep.get("sweep_mode") != str(sweep_mode):
            return None, None
        if sweep.get("signature_set") != SIGNATURE_SET_IDS[str(sweep_mode)]:
            return None, None
        samples_block = payload.get("samples")
        frontier_block = payload.get("frontier")
        if not isinstance(samples_block, dict) or not isinstance(frontier_block, dict):
            return None, None
        samples = _load_sample_list(samples_block.get(case_key))
        frontiers = _load_sample_list(frontier_block.get(case_key))
        if samples is None:
            return None, None
        if not samples:
            return None, None
        if frontiers is None:
            frontiers = pareto_frontier(
                samples, min_rel_improvement=FRONTIER_MIN_REL_ERROR_IMPROVEMENT
            )
        all_samples[case_key] = samples
        all_frontiers[case_key] = frontiers
    return all_samples, all_frontiers


def main() -> None:
    args = parse_args()
    json_stem = str(args.json_stem)
    sweep_mode = str(args.sweep_mode)
    all_samples, frontiers = load_plot_data_bundle(json_stem, sweep_mode)
    if all_samples is not None and frontiers is not None:
        print(
            f"[pareto] loaded plot data from {json_stem}_{sweep_mode}_{{solovev,chease,efit}}.json"
        )
        external_shape_errors = compute_selected_external_shape_errors(
            all_samples,
            backend=args.backend,
            test_nr=args.test_nr,
            test_nt=args.test_nt,
        )
        print_fastest_config_table_body(all_samples, external_shape_errors)
        render(
            all_samples,
            frontiers,
            SAVE_PNG_PATH,
            SAVE_PDF_PATH,
            args.test_nr,
            args.test_nt,
            args.backend,
        )
        return

    benchmark = load_benchmark(args.backend)
    cached_samples = load_cached_samples(json_stem, sweep_mode)
    progress = ParetoProgressDisplay(CASE_KEYS)
    case_signature_records: dict[str, list[SignatureRecord]] = {}
    for case_key in CASE_KEYS:
        min_lengths, max_lengths = get_case_length_bounds(case_key)
        case_seed = int(args.random_signature_seed) + sum(ord(ch) for ch in case_key)
        signature_records = generate_case_signatures(
            case_key,
            min_lengths,
            max_lengths,
            random_signature_count=args.random_signature_count,
            random_seed=case_seed,
            sweep_mode=sweep_mode,
        )
        case_signature_records[case_key] = signature_records
        progress.set_total(case_key, total=len(signature_records))
    all_samples: dict[str, list[Sample]] = {}
    try:
        for case_key in CASE_KEYS:
            all_samples[case_key] = sample_case(
                benchmark,
                case_key=case_key,
                backend=args.backend,
                test_nr=args.test_nr,
                test_nt=args.test_nt,
                repeat_count=args.repeat_count,
                random_signature_count=args.random_signature_count,
                random_signature_seed=args.random_signature_seed,
                signature_records=case_signature_records[case_key],
                cached_samples=cached_samples.get(case_key),
                progress=progress,
                sweep_mode=sweep_mode,
                case_workers=args.case_workers,
                case_worker_inner_threads=args.case_worker_inner_threads,
            )
            partial_frontiers = {
                key: pareto_frontier(samples, min_rel_improvement=args.frontier_min_rel_improvement)
                for key, samples in all_samples.items()
            }
            write_json(
                all_samples,
                partial_frontiers,
                json_stem,
                args.test_nr,
                args.test_nt,
                args.backend,
                sweep_mode,
            )
    finally:
        progress.close()
    frontiers = {
        case_key: pareto_frontier(samples, min_rel_improvement=args.frontier_min_rel_improvement)
        for case_key, samples in all_samples.items()
    }
    external_shape_errors = compute_selected_external_shape_errors(
        all_samples,
        backend=args.backend,
        test_nr=args.test_nr,
        test_nt=args.test_nt,
    )
    print_fastest_config_table_body(all_samples, external_shape_errors)
    render(
        all_samples,
        frontiers,
        SAVE_PNG_PATH,
        SAVE_PDF_PATH,
        args.test_nr,
        args.test_nt,
        args.backend,
    )
    write_json(
        all_samples,
        frontiers,
        json_stem,
        args.test_nr,
        args.test_nt,
        args.backend,
        sweep_mode,
    )


if __name__ == "__main__":
    main()
